    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
         int linha = tblItens.getSelectedRow();

    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Selecione um item na tabela para alterar a quantidade.");
        return;
    }

    int novaQtd = (Integer) spQuantidade.getValue();

    PedidoItem item = itensPedido.get(linha);
    item.setQuantidade(novaQtd);

    double subtotal = item.getSubtotal();
    String subtotalFormatado = "R$ " +
            String.format("%.2f", subtotal).replace('.', ',');

    modeloItens.setValueAt(novaQtd, linha, 1);      
    modeloItens.setValueAt(subtotalFormatado, linha, 3); 

    recalcularTotal();
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

    private void btnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItemActionPerformed
         int linha = tblItens.getSelectedRow();

    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Selecione um item para remover.");
        return;
    }

    itensPedido.remove(linha);          // tira da lista
    modeloItens.removeRow(linha);       // tira da tabela
    recalcularTotal();                  // atualiza o total                 
    }//GEN-LAST:event_btnRemoverItemActionPerformed

