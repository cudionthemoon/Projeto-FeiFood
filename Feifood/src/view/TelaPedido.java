    private void atualizarTotal() {
        String texto = String.format("%.2f", totalPedido).replace('.', ',');
        txtTotalPedido.setText("R$ " + texto);
    }
    
    private void preencherDataAtual() {
        DateTimeFormatter fmt = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        String agora = LocalDateTime.now().format(fmt);
        txtDataPedido.setText(agora);
    }
    
    private void adicionarItensSelecionados(java.util.List<PedidoItem> itensSelecionados) {
    itensPedido.clear();
    itensPedido.addAll(itensSelecionados);

    modeloItens.setRowCount(0);
    totalPedido = 0.0;

    for (PedidoItem item : itensPedido) {

        String nome = item.getAlimento().getNome();
        int qtd = item.getQuantidade();
        double precoUnit = item.getPrecoUnitario();
        double subtotal = item.getSubtotal();

        String precoFormatado = "R$ " +
                String.format("%.2f", precoUnit).replace('.', ',');
        String subtotalFormatado = "R$ " +
                String.format("%.2f", subtotal).replace('.', ',');

        modeloItens.addRow(new Object[]{
            nome,
            qtd,
            precoFormatado,
            subtotalFormatado
        });

        totalPedido += subtotal;
    }

    atualizarTotal();
}
    
    private void recalcularTotal() {
    totalPedido = 0.0;
    for (PedidoItem item : itensPedido) {
        totalPedido += item.getSubtotal();
    }
    atualizarTotal();
}



